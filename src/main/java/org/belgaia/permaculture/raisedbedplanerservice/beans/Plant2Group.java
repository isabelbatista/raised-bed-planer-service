package org.belgaia.permaculture.raisedbedplanerservice.beans;

public class Plant2Group {

    private Integer plantGroupId;
    private String plantGroupName;
    private Integer plantId;
    private String plantName;

    public Integer getPlantGroupId() {
        return plantGroupId;
    }

    public void setPlantGroupId(Integer plantGroupId) {
        this.plantGroupId = plantGroupId;
    }

    public String getPlantGroupName() {
        return plantGroupName;
    }

    public void setPlantGroupName(String plantGroupName) {
        this.plantGroupName = plantGroupName;
    }

    public Integer getPlantId() {
        return plantId;
    }

    public void setPlantId(Integer plantId) {
        this.plantId = plantId;
    }

    public String getPlantName() {
        return plantName;
    }

    public void setPlantName(String plantName) {
        this.plantName = plantName;
    }
}
